    def _set_status(self, status: AgentStatus) -> None:
        """Update status and emit event."""
        if self.status != status:
            self.status = status
            if self.observer:
                self.observer.on_status_changed(self)

    # Valid response state transitions (from -> set of valid targets)
    _VALID_RESPONSE_TRANSITIONS: dict[ResponseState, set[ResponseState]] = {
        ResponseState.IDLE: {ResponseState.STREAMING, ResponseState.IDLE},
        ResponseState.STREAMING: {ResponseState.IDLE, ResponseState.INTERRUPTED},
        ResponseState.INTERRUPTED: {ResponseState.IDLE},
    }

    def _set_response_state(self, new_state: ResponseState) -> None:
        """Transition the response state machine with validation.

        Invalid transitions are logged as warnings but allowed to proceed
        to avoid breaking existing behavior during the migration.
        """
        old = self._response_state
        valid = self._VALID_RESPONSE_TRANSITIONS.get(old, set())
        if new_state not in valid:
            log.warning(
                "Invalid response state transition: %s -> %s (agent=%s)",
                old,
                new_state,
                self.name,
            )
        if old != new_state:
            log.debug("Response state: %s -> %s (agent=%s)", old, new_state, self.name)
        self._response_state = new_state

    # Valid permission modes
    PERMISSION_MODES = {
        "default",
        "acceptEdits",
        "plan",
        "planSwarm",
        "bypassPermissions",
    }

    def _set_permission_mode_local(self, mode: str) -> None:
        """Update permission mode locally without calling SDK.

        Used when SDK already knows (e.g., EnterPlanMode/ExitPlanMode tools).
        """
        assert mode in self.PERMISSION_MODES, f"Invalid permission mode: {mode}"
        if self.permission_mode != mode:
            self.permission_mode = mode
            if self.observer:
                self.observer.on_permission_mode_changed(self)

    async def _sync_permission_mode_to_sdk(self, mode: str) -> None:
        """Send permission mode to SDK without changing local state.

        Use this when local state was already set via ``_set_permission_mode_local``
        but the SDK still needs to be notified.

        Because this runs as a deferred task (via ``create_safe_task``), the
        local state may have changed between scheduling and execution.  If so,
        skip the SDK call — whoever changed the mode is responsible for syncing.
        """
        if self.permission_mode != mode:
            return  # Local state changed since we were scheduled; skip
        if self.client and self.session_id:
            try:
                await self.client.set_permission_mode(cast(PermissionMode, mode))
            except Exception:
                log.warning(
                    "Deferred sync of permission mode '%s' to SDK failed"
                    " (control request timeout)",
                    mode,
                )

    async def ensure_plan_path(self) -> None:
        """Fetch and cache the plan path for this session (if not already set)."""
        if self.session_id and not self.plan_path:
            self.plan_path = await get_plan_path_for_session(
                self.session_id, cwd=self.cwd, must_exist=False
            )

    async def set_permission_mode(self, mode: str) -> None:
        """Update permission mode via SDK and emit event.

        Args:
            mode: One of 'default', 'acceptEdits', 'plan'
        """
        assert mode in self.PERMISSION_MODES, f"Invalid permission mode: {mode}"
        if self.permission_mode != mode:
            self.permission_mode = mode
            # Fetch plan path when entering plan mode
            if mode == "plan":
                await self.ensure_plan_path()
            # Only call SDK if connected (client exists and has active connection)
            if self.client and self.session_id:
                try:
                    await self.client.set_permission_mode(cast(PermissionMode, mode))
                except Exception:
                    log.warning(
                        "Failed to sync permission mode '%s' to SDK"
                        " (control request timeout); local state updated",
                        mode,
                    )
            if self.observer:
                self.observer.on_permission_mode_changed(self)

    def _build_message_with_images(self, prompt: str) -> dict[str, Any]:
        """Build SDK message with text and images."""
        content: list[dict[str, Any]] = []
        if prompt.strip():
            content.append({"type": "text", "text": prompt})
        for img in self.pending_images:
            content.append(
                {
                    "type": "image",
                    "source": {
                        "type": "base64",
                        "media_type": img.media_type,
                        "data": img.base64_data,
                    },
                }
            )
        return {
            "type": "user",
            "message": {"role": "user", "content": content},
            "parent_tool_use_id": None,
        }

    def get_background_processes(self) -> list:
        """Get list of background processes for this agent.

        Returns:
            List of BackgroundProcess objects (with output_file if known)
        """
        if not self._claude_pid:
            return []
        from claudechic.processes import get_child_processes

        processes = get_child_processes(self._claude_pid)

        # Enrich with output files if we have them
        for proc in processes:
            if proc.command in self._background_outputs:
                # Create new instance with output_file set
                proc.output_file = self._background_outputs[proc.command]

        return processes
