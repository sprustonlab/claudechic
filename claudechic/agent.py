# ---------------------------------------------------------------------------
# Message types for chat history
# ---------------------------------------------------------------------------


@dataclass
class ImageAttachment:
    """An image attached to a message."""

    path: str
    filename: str
    media_type: str
    base64_data: str


@dataclass
class UserContent:
    """A user message in chat history."""

    text: str
    images: list[ImageAttachment] = field(default_factory=list)


@dataclass
class ToolUse:
    """A tool use within an assistant turn."""

    id: str
    name: str
    input: dict[str, Any]
    parent_tool_use_id: str | None = None
    result: str | None = None
    is_error: bool = False


@dataclass
class TextBlock:
    """A text block within an assistant turn."""

    text: str


@dataclass
class AssistantContent:
    """An assistant message in chat history.

    Contains an ordered list of blocks (TextBlock or ToolUse) to preserve
    the original interleaving of text and tool uses.
    """

    blocks: list[TextBlock | ToolUse] = field(default_factory=list)


@dataclass
class MessageMetadata:
    """Metadata for a chat message (timestamp, model, token usage, etc.)."""

    timestamp: str | None = None  # ISO 8601 timestamp (e.g., "2026-03-14T13:30:22.369Z")
    model: str | None = None  # Model name (e.g., "claude-haiku-4-5-20251001")
    input_tokens: int | None = None  # Tokens consumed by the message
    output_tokens: int | None = None  # Tokens generated by the message
    cache_creation_tokens: int | None = None  # New cache tokens created
    cache_read_tokens: int | None = None  # Cached tokens reused
    duration_ms: int | None = None  # Response duration in milliseconds
    cost_usd: float | None = None  # Cost in USD
    message_id: str | None = None  # UUID of the message


@dataclass
class ChatItem:
    """A single item in chat history."""

    role: Literal["user", "assistant"]
    content: UserContent | AssistantContent
    metadata: MessageMetadata | None = None  # Optional metadata about the message


# ---------------------------------------------------------------------------
# Response state tracking
# ---------------------------------------------------------------------------


@dataclass
class ResponseContext:
    """Per-response mutable state, reset at the start of each response.

    Groups the scattered boolean/nullable flags that were previously spread
    across Agent.__init__. All fields are reset by ``Agent._start_response()``.
    """

    task: asyncio.Task[None] | None = None
    current_assistant: AssistantContent | None = None
    text_buffer: str = ""
    needs_new_message: bool = True
    had_tools: bool = False


